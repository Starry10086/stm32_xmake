#ifndef BL_PARTITION_H
#define BL_PARTITION_H

#include <stdint.h>

#define BL_FLASH_BASE_ADDR  0x08000000UL
#define BL_FLASH_TOTAL_SIZE 0x00100000UL
#define BL_FLASH_END_ADDR   (BL_FLASH_BASE_ADDR + BL_FLASH_TOTAL_SIZE - 1UL)

#define BL_BOOT_START_ADDR  0x08000000UL
#define BL_BOOT_SIZE        0x00010000UL
#define BL_BOOT_END_ADDR    (BL_BOOT_START_ADDR + BL_BOOT_SIZE - 1UL)

#define BL_PARAM_START_ADDR 0x08010000UL
#define BL_PARAM_SIZE       0x00010000UL
#define BL_PARAM_END_ADDR   (BL_PARAM_START_ADDR + BL_PARAM_SIZE - 1UL)

#define BL_APP1_START_ADDR  0x08020000UL
#define BL_APP1_SIZE        0x00060000UL
#define BL_APP1_END_ADDR    (BL_APP1_START_ADDR + BL_APP1_SIZE - 1UL)

#define BL_APP2_START_ADDR  0x08080000UL
#define BL_APP2_SIZE        0x00080000UL
#define BL_APP2_END_ADDR    (BL_APP2_START_ADDR + BL_APP2_SIZE - 1UL)

#define BL_SRAM_START_ADDR  0x20000000UL
#define BL_SRAM_SIZE        0x00020000UL
#define BL_SRAM_END_ADDR    (BL_SRAM_START_ADDR + BL_SRAM_SIZE - 1UL)
#define BL_SRAM_TOP_ADDR    (BL_SRAM_START_ADDR + BL_SRAM_SIZE)

#endif